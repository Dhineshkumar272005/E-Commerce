import { useContext, useState } from "react";
import { useForm } from "react-hook-form";
import { AuthContext } from "../context/AuthContext";
import { useNavigate } from "react-router-dom";

export default function Auth() {
    const [mode, setMode] = useState("signup");
    const [error, seterror] = useState(null);
    const navigate = useNavigate()


    const { register, handleSubmit, formState: { errors } } = useForm();
    const { signup, login, user, logout } = useContext(AuthContext);

    function onSubmit(data) {
        seterror(null);
        let result;
        if (mode === "signup") {
            result = signup(data.email, data.password);
        } else {
            result = login(data.email, data.password);
        }
        if (result.success) {
            navigate("/");
        } else {
            seterror(result.error);
        }

        console.log(result);
    }
    return (
        <div className="page">
            <div className="container">
                {user && <p>User logged in: {user.email} </p>}
                <button className="btn btn-primary" onClick={() => logout()}>Logout</button>
                <div className="auth-container">
                    <h1 className="page-title">
                        {mode === "signup" ? "Sign Up" : "Login"}
                    </h1>
                    <form className="auth-form" onSubmit={handleSubmit(onSubmit)}>
                        {error && <div className="error-message">{error}</div>}
                        <div className="form-group">
                            <label className="form-label" htmlFor="email">Email</label>
                            <input className="form-input" type="email" id="email" {...register('email', { required: "Email is required" })} />
                        </div>
                        <div className="form-group">
                            <label className="form-label">Password</label>
                            <input {...register('password', { required: "Password is required", minLength: { value: 6, message: "Password must be at least 6 characters", }, maxLength: { value: 12, message: "Password must be less than 12 characters", }, })} id="password" className="form-input" type="password" />
                        </div>
                        <button type="submit" className="btn btn-primary btn-large">{mode === "signup" ? "Sign Up" : "Login"}</button>
                    </form>
                    <div className="auth-switch">
                        {mode === "signup" ? (
                            <p>Already have an account <span className="auth-link" onClick={() => setMode("login")}>Login</span></p>
                        ) : (
                            <p>
                                Don't have an account?{" "}
                                <span className="auth-link" onClick={() => setMode("signup")}>Sign up</span>
                            </p>
                        )}
                    </div>
                </div>
            </div>
        </div>
    );
}
